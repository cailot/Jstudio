package hyung.jin.seo.m7k37;

public class M7k37Menu {
	private int icon;
	private String name;
	private String desc;
	
	public int getIcon()
	{
		return icon;
	}
	public void setIcon(int icon)
	{
		this.icon = icon;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public M7k37Menu(String name, String desc) {
		super();
		this.name = name;
		this.desc = desc;
	}
	public M7k37Menu(int icon, String name, String desc) {
		super();
		this.icon = icon;
		this.name = name;
		this.desc = desc;
	}
	@Override
	public String toString() {
		return "M7k37Menu [icon=" + icon + ", name=" + name + ", desc=" + desc + "]";
	}

}